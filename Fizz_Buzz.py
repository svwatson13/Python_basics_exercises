def fizz_buzz(num):
    if num%3 == 0 and num%5 == 0:
        return 'Bizzuu'
    if num%3 == 0:
        return 'Bizz'
    if num%5 == 0:
        return 'Zzuu'
    else:
        return 'Loser'
print(fizz_buzz(7))
